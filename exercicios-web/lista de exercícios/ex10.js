// Crie uma função que verifica se um número inteiro passado como parâmetro é divisível por 3 e retorne true
// ou false.

const divisívelPor3 = numero => numero % 3 == 0 ? true : false;

console.log(divisívelPor3(6));
console.log(divisívelPor3(5));
console.log(divisívelPor3(1));
console.log(divisívelPor3(3));